    int j; // biến lưu lại vị trí ôn tập
    File fileCountReview = new File("manage\\countReview.txt"); // file quản lí biên ôn tập
    /**
     * hàm thiết lập khi mở cửa sổ chính thì show ra màn hình ( tự định nghĩa )
     * @param evt 
     */
    private void formWindowOpenedNew(ActionEvent evt) {                                  
        PhienAm.setText(null);
        TuLoai.setText(null);
        Nghia.setText(null);
        LabelImage.setIcon(null);
        try {
            Scanner sc = new Scanner(fileCountReview);
            j = sc.nextInt();
        } catch (FileNotFoundException ex) {
            Logger.getLogger(HocTuVung.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        TuVung wordTemp = allWord.getDanhSachTu().get(j);
        WordShow.setText(wordTemp.getTenTu());
        DAUHOI.setText("????");
        ChuDe topicTemp = new ChuDe();
            for( ChuDe cd : BienToanCuc.duLieu1.getDanhSachChuDe() ) 
                for(TuVung tu : cd.getDanhSachTu() ) {
                    if( tu == wordTemp ) { topicTemp = cd; break; }
            }
        CHUDE.setText(topicTemp.getTenChuDe());
    }
    /**
     * thiết lập hiển thị đáp án
     * @param evt 
     */
    private void DAPANActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DAPANActionPerformed
        TuVung wordTemp = new TuVung();
        ChuDe topicTemp = new ChuDe();
        for( ChuDe cd : BienToanCuc.duLieu1.getDanhSachChuDe() ) 
                for(TuVung tv : cd.getDanhSachTu() ) {
                    if( tv.getTenTu().equals(WordShow.getText()) ) { topicTemp = cd; wordTemp = tv; break; }
            }
        CHUDE.setText(topicTemp.getTenChuDe());
        DAUHOI.setText(null);
        PhienAm.setText(wordTemp.getPhienAm());
        TuLoai.setText(wordTemp.getTuLoai());
        Nghia.setText(wordTemp.getNghia());
        // điều chỉnh kích thước cho ảnh vừa với Label
        ImageIcon icon = new ImageIcon(wordTemp.getLinkImage());
        Image image = icon.getImage().getScaledInstance(LabelImage.getWidth(), LabelImage.getHeight(), Image.SCALE_SMOOTH);
        icon = new ImageIcon(image, icon.getDescription());
        LabelImage.setIcon(icon);
    }//GEN-LAST:event_DAPANActionPerformed
    /**
     * chuyển câu hỏi
     * @param evt 
     */
    private void TIEPActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TIEPActionPerformed
        try {
            // kiem tra so tu da hoc, va khi on tap
            File fileCount = new File("manage\\count.txt");
            Scanner scCount = new Scanner(fileCount);
            int i = scCount.nextInt();
            if( j >= i ) { JOptionPane.showMessageDialog(this,"Đã ôn tập hết từ đã học","Message Dialog Demo",JOptionPane.INFORMATION_MESSAGE); return; }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(OnTapTuVung.class.getName()).log(Level.SEVERE, null, ex);
        }
        // hien thi tren man hinh
        PhienAm.setText(null);
        TuLoai.setText(null);
        Nghia.setText(null);
        LabelImage.setIcon(null);
        j++;
        TuVung wordTemp = allWord.getDanhSachTu().get(j);
        WordShow.setText(wordTemp.getTenTu());
        DAUHOI.setText("????");
        ChuDe topicTemp = new ChuDe();
            for( ChuDe cd : BienToanCuc.duLieu1.getDanhSachChuDe() ) 
                for(TuVung tu : cd.getDanhSachTu() ) {
                    if( tu == wordTemp ) { topicTemp = cd; break; }
            }
        CHUDE.setText(topicTemp.getTenChuDe());
    }//GEN-LAST:event_TIEPActionPerformed
