    /**
     * thiết lập để ôn tập lại từ đầu
     * @param evt 
     */
    private void RESETActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RESETActionPerformed
        int check = JOptionPane.showConfirmDialog(null,"Bạn có thực sự muốn ôn tập lại từ đầu?","", JOptionPane.YES_NO_OPTION);
        if( check == 0 ) {
            try {
                File fileCount = new File("manage\\countReview.txt"); 
                FileWriter fw = new FileWriter(fileCount.getAbsoluteFile());
                fw.write(String.valueOf(0));
                fw.close();
                JOptionPane.showMessageDialog(this,"XONG","Message Dialog Demo",JOptionPane.INFORMATION_MESSAGE);
            } catch (IOException ex) {
                Logger.getLogger(HocTuVung.class.getName()).log(Level.SEVERE, null, ex);
            }
            formWindowOpenedNew(evt);
        }
    }//GEN-LAST:event_RESETActionPerformed
